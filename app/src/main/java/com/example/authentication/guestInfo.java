package com.example.authentication;

public class guestInfo {


    String name;
    String city;
    String quater_no;
    String hostID;

    public guestInfo() {
    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getQuater_no() {
        return quater_no;
    }

    public void setQuater_no(String quater_no) {
        this.quater_no = quater_no;
    }

    public String getHostID() {
        return hostID;
    }

    public void setHostID(String hostID) {
        this.hostID = hostID;
    }
}
